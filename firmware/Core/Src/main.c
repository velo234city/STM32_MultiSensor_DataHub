uint8_t Read_Tilt_Switch(void)
{
    uint8_t state1, state2;
    
    state1 = TILT_READ();
    HAL_Delay(10);  // Short delay
    state2 = TILT_READ();
    
    if (state1 == state2) {
        return state1;  // Readings match, return stable state
    } else {
        // If inconsistent, read one more time (simplified)
        return TILT_READ();
    }
}

